package com.solipsism.governmentexamportal;

public class Exam {

    private String age;
    private String desc;
    private String gender;
    private String marks10;
    private String marks12;
    private String marksGrad;

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMarks10() {
        return marks10;
    }

    public void setMarks10(String marks10) {
        this.marks10 = marks10;
    }

    public String getMarks12() {
        return marks12;
    }

    public void setMarks12(String marks12) {
        this.marks12 = marks12;
    }

    public String getMarksGrad() {
        return marksGrad;
    }

    public void setMarksGrad(String marksGrad) {
        this.marksGrad = marksGrad;
    }
}
